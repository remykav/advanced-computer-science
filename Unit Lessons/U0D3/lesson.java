public class Lesson{

    public static void main(String[] args) {
        //variables
        int number = 5; // assignment operator
        // single = assigns a value
        // different from == which comapres a value

        number += 6; // this is the same as number = number + 6
        number -= 3; // this is the same as number = number -3
        number *= 10; // this is the same as number = number * 10
        number /= 2; // this is the same as number = number /2

        // increment and decrement operators
        number += 1;
        number ++;

        // useful for loops
        number -= 1;
        number --;

        // boolean operators, evaluate to true or false
        // use them in if statements
        /* 
        YOU CANNOT USE THESE FOR OBJECTS
        number == 5;
        number != 10;
        number <= 15;
        number >= 20; 
        /* */
    }




}